from __future__ import annotations
import asyncio, os, sys, json, time, datetime, re
BASE_DIR = os.path.dirname(os.path.abspath(__file__))
sys.path.insert(0, BASE_DIR)
COOKIE_FILE  = os.path.join(BASE_DIR, "cookie.txt")
EMAILS_FILE  = os.path.join(BASE_DIR, "emails.txt")
EXPORT_FILE  = os.path.join(BASE_DIR, "emails_export.txt")

EDIT_COOKIE_HEADER = """// Semicolon separated Cookie File\n// This file was generated by EditThisCookie"""

# ─────────────────────────────────────────
