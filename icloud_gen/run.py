async def cmd_generate():
    cookies = load_cookies()
    # Accepte le count depuis sys.argv[2] (ex: python3 run.py generate 5)
    # Sinon demande interactivement
    if len(sys.argv) > 2:
        try:
            n = int(sys.argv[2])
            print(f"\n📧 Génération de {n} email(s)...")
        except ValueError:
            n = 5
    else:
        try:
            n = int(input("\n📧 Combien d'emails à générer ? (max 25 recommandé) : ").strip())
        except (ValueError, EOFError):
            n = 5

    all_emails = []
    async with ICloudHME(cookies) as hme:
        generated = 0
        while generated < n:
            batch = min(n - generated, 10)
            print(f"\n  Génération batch {generated+1}–{generated+batch}...")
            emails = await hme.generate_batch(batch)
            for e in emails:
                print(f"  ✅ {e}")
            all_emails.extend(emails)
            generated += batch
            if generated < n:
                print(f"  ⏳ Pause 5s (limite API Apple)...")
                await asyncio.sleep(5)

    if all_emails:
        with open(EMAILS_FILE, "a", encoding="utf-8") as f:
            f.write("\n".join(all_emails) + "\n")
        print(f"\n✅ {len(all_emails)} emails sauvegardés dans emails.txt")
    else:
        print("\n❌ Aucun email généré. Vérifiez cookie.txt (relancez : python3 run.py login)")

async def cmd_list():
    cookies = load_cookies()
    async with ICloudHME(cookies) as hme:
        emails = await hme.list_all()

    if not emails:
        print("Aucun email trouvé (ou session expirée).")
        return

    active   = [e for e in emails if e.get("isActive")]
    inactive = [e for e in emails if not e.get("isActive")]

    print(f"\n{'─'*62}")
    print(f"  📧  {len(active)} emails actifs   |   {len(inactive)} désactivés")
    print(f"{'─'*62}")
    print(f"  {'EMAIL':<38} {'CRÉÉ LE':<18}")
    print(f"{'─'*62}")
    for e in active:
        ts = datetime.datetime.fromtimestamp(e.get("createTimestamp",0)/1000).strftime("%d/%m/%Y %H:%M")
        print(f"  {e['hme']:<38} {ts}")
    print(f"{'─'*62}\n")

    # Sauvegarde aussi dans emails_export.txt
    with open(EXPORT_FILE, "w", encoding="utf-8") as f:
        f.write(f"# Export iCloud Hide My Email — {datetime.datetime.now():%d/%m/%Y %H:%M}\n")
        f.write(f"# {len(active)} emails actifs\n\n")
        for e in active:
            ts = datetime.datetime.fromtimestamp(e.get("createTimestamp",0)/1000).strftime("%d/%m/%Y %H:%M")
            f.write(f"{e['hme']}  (créé le {ts})\n")
    print(f"📄 Listing complet sauvegardé dans emails_export.txt")

async def cmd_export():
    """Relit emails.txt et affiche un tableau propre."""
    if not os.path.exists(EMAILS_FILE):
        print("❌ emails.txt vide. Générez des emails d'abord.")
        return
    with open(EMAILS_FILE) as f:
        emails = [l.strip() for l in f if l.strip()]
    print(f"\n{'─'*50}")
    print(f"  📧  {len(emails)} emails dans emails.txt")
    print(f"{'─'*50}")
    for i, e in enumerate(emails, 1):
        print(f"  {i:>3}.  {e}")
    print(f"{'─'*50}\n")

